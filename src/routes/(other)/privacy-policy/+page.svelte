	<h1 class="text-2xl">World Affairs Conference (WAC) Privacy Policy</h1>

	<h2 class="text-xl">I. INTRODUCTION</h2>

	<p>
		We, at World Affairs Conference (<em>WAC</em>), respect the privacy of
		our participants and are committed to protecting their personal
		information. This Privacy Policy describes how we collect, use, and
		protect personal information provided by participants through our
		conference registration website.
