		successful execution of the conference.
	</p>

	<h2 class="text-xl">III. USE OF INFORMATION</h2>

	<p>
		The aforementioned information is used for the purpose of conference
		planning, organizing, communication, and ensuring dietary standards are
		met. This usage is solely for the internal purposes of WAC.
	</p>

	<h2 class="text-xl">IV. STORAGE & DELETION OF INFORMATION</h2>

	<p>
