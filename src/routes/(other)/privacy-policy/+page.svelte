		the conclusion of the conference. The collected personal information
		will also be stored securely in a database located in the State of
		Virginia, United States of America, provided by our partner, Xatabase,
		Inc.
	</p>

	<h2 class="text-xl">V. DISCLOSURE OF INFORMATION</h2>

	<p>
		We do not disclose, sell, or share the collected personal data with any
		third parties, except for our database provider, Xatabase, Inc. This
		sharing of data is strictly for the purpose of secure data storage and
		does not involve any other use of the data by Xatabase, Inc.
	</p>

	<h2 class="text-xl">VI. LOCATION & INTERNATIONAL DATA TRANSFER</h2>

	<p>
		We are based in Toronto, Canada. However, please note that the data we
		collect is stored by our database provider in the United States of
		America, which causes an international data transfer to take place.
	</p>

	<h2 class="text-xl">VII. CHANGES TO PRIVACY POLICY</h2>

	<p>
		This privacy policy is subject to change. All changes will be posted on
		our website in due course.
